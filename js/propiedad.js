export function Propiedad(id, titulo, transaccion, descripcion, precio, numBanios, numAutos, numDormitorios)
{
    this.id = id;
    this.titulo = titulo;
    this.transaccion = transaccion;
    this.descripcion = descripcion;
    this.precio = precio;
    this.numBanios = numBanios;
    this.numAutos = numAutos;
    this.numDormitorios = numDormitorios;
}